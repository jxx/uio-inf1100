from math import sqrt, pi, e
from pprint import pprint
import numpy as np

def h(x):
    return (1/sqrt(2*pi)) * e**(-0.5*(x**2))

n = 41  # number of points along the x axis
dx = 4.0/((n-1)/2) # spacing between x points

x = np.zeros(n)
y = np.zeros(n)

for i in xrange((-n+1)/2,(n+1)/2): 
    x[i] = i * dx
    y[i] = h(x[i])

pprint(x)
pprint(y)

